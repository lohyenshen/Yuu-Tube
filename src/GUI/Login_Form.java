    private void jTextField1_emailFocusGained(java.awt.event.FocusEvent evt) {
        // clear the textfield on focus if the text is "username"
        if(jTextField1_email.getText().trim().toLowerCase().equals("e-mail")){
            jTextField1_email.setText("");
            jTextField1_email.setForeground(Color.BLACK);

        }

        //set a yellow border to the text box
        Border jlabel_icon = BorderFactory.createMatteBorder(1, 1, 1, 1, new Color(166,166,166));

        //set the border to the jpanel_title
        jLabel1.setBorder(jlabel_icon);
    }

    private void jTextField1_emailFocusLost(java.awt.event.FocusEvent evt) {
        //if the text field is equal to username or empty
        //will set the "username" text in the field
        //on focus lost

        if(jTextField1_email.getText().trim().toLowerCase().equals("e-mail")||
                jTextField1_email.getText().trim().toLowerCase().equals("")){
            jTextField1_email.setText("e-mail");
            jTextField1_email.setForeground(new Color(153, 153, 153));
        }
        jLabel1.setBorder(null);
    }

    private void jPasswordField1_PasswordFocusGained(java.awt.event.FocusEvent evt) {
        String pass = String.valueOf(jPasswordField1_Password.getPassword());

        if(pass.trim().toLowerCase().equals("password")){
            jPasswordField1_Password.setText("");
            jPasswordField1_Password.setForeground(Color.BLACK);
        }


        //set a yellow border to the text box
        Border jlabel_icon = BorderFactory.createMatteBorder(1, 1, 1, 1, new Color(166,166,166));

        //set the border to the jpanel_title
        jLabel2.setBorder(jlabel_icon);
    }

    private void jPasswordField1_PasswordFocusLost(java.awt.event.FocusEvent evt) {

        String pass = String.valueOf(jPasswordField1_Password.getPassword());
        if(pass.trim().toLowerCase().equals("password")||
                pass.trim().toLowerCase().equals("")){
            jPasswordField1_Password.setText("password");
            jPasswordField1_Password.setForeground(new Color(153, 153, 153));
        }

        jLabel2.setBorder(null);
    }

